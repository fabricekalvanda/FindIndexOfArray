using System;

namespace FindIndexOfArray
{
    class Program
    {
        static void Main(string[] args)
        {
            int[] arrayy = { -8, 2, 6, 2, -5, 4, 1 }; 

            for (int i = 0; i < arrayy.Length; i++)
            {
                Console.Write(arrayy[i] + " ");
            }
        }
    }
}
